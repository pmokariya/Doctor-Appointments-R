# Patient Portal
A patient portal built in Ruby on Rails modeled after Intermountain Healthcare's Patient Portal

## Goals For Project

* Create models for doctors and patients, with has many: through relationship for appointments
* Create controllers that manage all CRUD actions for each model.
* Create Views.
* Allow doctors or employees to input notes to patient file
* Allow creation of patients and editing patients

## Long Term Goals
* Launch on Heroku

## Built Using
- HTML
- CSS/SCSS
- Ruby on Rails
- Devise (auth)
- Faker gem for fill data


